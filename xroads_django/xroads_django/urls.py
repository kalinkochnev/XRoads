from django.contrib import admin
from django.urls import path, include, re_path
from dj_rest_auth.registration.views import VerifyEmailView
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

schema_view = get_schema_view(
   openapi.Info(
      title="Xroads API",
      default_version='v1',
      description="This is where you can find info on how to use the endpoints for Xroads",
      terms_of_service="not currently available",
      contact=openapi.Contact(email="kalin.kochnev@gmail.com"),
      license=openapi.License(name="Not currently available"),
   ),
   public=False,
   permission_classes=(permissions.IsAdminUser,),
)

urlpatterns = [
    re_path(r'^swagger(?P<format>\.json|\.yaml)$', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    re_path(r'^swagger/$', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    re_path(r'^redoc/$', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),

    path('api/', include('XroadsAPI.urls')),
    path('admin/', admin.site.urls),
    path('auth/', include('XroadsAuth.urls')),
]
